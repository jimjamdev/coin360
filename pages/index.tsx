import type { NextPage } from 'next'
import Head from 'next/head'
import {getCoins} from '../providers'
import styles from '../styles/Home.module.scss'

interface ICoinData {
  coinData: {
    data: [{
      s: string;
      p: string;
      ch: string;
    }];
    error: string;
  }
}

const Home: NextPage<ICoinData> = ({coinData}) => {
  const {data = [], error = ''} = coinData;
  return (
    <div className={styles.container}>
      <Head>
        <title>Coin360</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      {error && error}
      <table>
      {data && data.map((coin) => {
        return <tr key={coin.s}><td>{coin.s}</td> <td>{coin.p}</td> <td>{coin.ch}</td></tr>
      })}
      </table>
    </div>
  )
}

export async function getStaticProps() {
  const coinData = await getCoins()
  return {
    props: {coinData}, // will be passed to the page component as props
  }
}

export default Home
